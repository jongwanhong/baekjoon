# 수 자료형(Number)
# 정수형
a = 1000 # 양의 정수
print(a)

a = -7 # 음의 정수
print(a)

a = 0 #0
print(a)

# 실수형
a = 157.93 # 양의 실수
print(a)

a = -1494.44 # 음의 실수
print(a)

a = 5. # 소수부가 0일 때 0 생략
print(a)

a = -.7 # 정수부가 0일 때 0 생략
print(a)

a = 2e9 # 10억을 표현할 때 사용할 수 있
print(a)

a = 75.25e1
print(a)

a = 3954e-3
print(a)